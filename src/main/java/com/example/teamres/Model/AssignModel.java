package com.example.teamres.Model;

public class AssignModel {

    private int id;
    private String assn;
    String date;
    private int status;



    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getAssn() {
        return assn;
    }

    public void setAssn(String assn) {
        this.assn = assn;
    }


}
